def load_source(path,img_sz=640,stride=32):
    # Read image
    img0 = cv2.imread(path)  # BGR
    assert img0 is not None, f'Image Not Found {path}'

    # Padded resize
    img = letterbox(img0, img_sz, stride=stride, auto=True)[0]

    # Convert
    img = img.transpose((2, 0, 1))[::-1]  # HWC to CHW, BGR to RGB
    img = np.ascontiguousarray(img)
    return img


def model_load(weights,device,data,dnn=False,half=False,bs=1,img_sz=320):
    # Load model
    device = select_device(device)
    model = DetectMultiBackend(weights, device=device, dnn=dnn, data=data, fp16=half)
    stride, names, pt = model.stride, model.names, model.pt
    imgsz = check_img_size(img_sz, s=stride)  # check image size
    imgsz = [imgsz,imgsz]
    model.warmup(imgsz=(1 if pt else bs, 3, *imgsz))  # warmup
    return model 

